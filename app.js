
const { MongoClient } = require('mongodb');

async function main() {
  const uri = 'mongodb://localhost:27017'; // URI de conexão com o MongoDB
const client = new MongoClient(uri);

try {
    await client.connect(); // Conecta ao MongoDB

    const database = client.db('meubanco'); // Nome do banco de dados
    const collection = database.collection('minhacolecao'); // Nome da coleção

    // Inserindo um documento
    await collection.insertOne({ name: 'Alice', age: 30 });

    // Consultando documentos
    const result = await collection.find({ name: 'Alice' }).toArray();
    console.log(result);
} finally {
    await client.close(); // Fecha a conexão
}
}




